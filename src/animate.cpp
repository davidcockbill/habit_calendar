static const uint32_t NO_OF_FRAMES = sizeof FRAMES / sizeof FRAMES[0]; 


Animate::Animate()
{
}

void Animate::animate(LedMatrix &matrix)
{
    uint16_t interval_ms = 100;
    for (uint8_t frameNumber = 0; frameNumber < NO_OF_FRAMES; ++frameNumber)
    {
        for (uint8_t month = 0; month < 12; ++month)
        {
            matrix.setDays(month, FRAMES[frameNumber][month]);
        }
        delay(interval_ms);
    }
}